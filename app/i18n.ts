export const LANGUAGE_OPTIONS = [
  { code: "en-US", flag: "🇺🇸", label: "English (United States)" },
  { code: "en-GB", flag: "🇬🇧", label: "English (United Kingdom)" },
  { code: "es-MX", flag: "🇲🇽", label: "Español (México)" },
  { code: "es-CO", flag: "🇨🇴", label: "Español (Colombia)" },
  { code: "es-ES", flag: "🇪🇸", label: "Español (España)" },
  { code: "es-PR", flag: "🇵🇷", label: "Español (Puerto Rico)" },
  { code: "es-SA", flag: "🌎", label: "Español (South America)" },
  { code: "pt-BR", flag: "🇧🇷", label: "Português (Brasil)" },
  { code: "fr",    flag: "🇫🇷", label: "Français" },
  { code: "ht",    flag: "🇭🇹", label: "Kreyòl Ayisyen" },
  { code: "zh-CN", flag: "🇨🇳", label: "中文 (简体)" },
] as const;

export type LangCode = typeof LANGUAGE_OPTIONS[number]["code"];

export function getLangFamily(code: string): "en" | "es" | "pt-BR" | "fr" | "ht" | "zh-CN" {
  if (code.startsWith("en")) return "en";
  if (code.startsWith("es")) return "es";
  if (code === "pt-BR") return "pt-BR";
  if (code === "fr") return "fr";
  if (code === "ht") return "ht";
  if (code === "zh-CN") return "zh-CN";
  return "en";
}

export function getLangName(code: string): string {
  const opt = LANGUAGE_OPTIONS.find(l => l.code === code);
  return opt?.label ?? "English";
}

export function readLang(): string {
  if (typeof window === "undefined") return "en-US";
  const v = localStorage.getItem("companion_language") ?? "en-US";
  // backwards compat: old codes "en" → "en-US", "es" → "es-MX"
  if (v === "en") return "en-US";
  if (v === "es") return "es-MX";
  return v;
}

